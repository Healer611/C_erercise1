#include <assert.h>
#include <stdio.h>
#include <string.h>

#define MAX 100
#define MAX_NAME 20
#define MAX_SEX 5
#define MAX_TELE 12
#define MAX_ADDR 30

enum OPPION
{
	EXIT,	//退出
	ADD,	//添加
	DEL,	//删除
	SEARCH, //查找
	MODIFY, //修改
	SHOW,   //打印
	SORT    //排序
};


//类型的声明
typedef struct PeoInfo
{
	char name[MAX_NAME];
	int age;
	char sex[MAX_SEX];
	char tele[MAX_TELE];
	char addr[MAX_ADDR];
}PeoInfo;

//通讯录
typedef struct Contact
{
	PeoInfo data[100];
	int sz;
}Contact;


//初始化函数声明
void InitContact(Contact* pc);

